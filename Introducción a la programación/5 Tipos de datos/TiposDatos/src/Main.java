public class Main {
    public static void main(String[] args) {

        //Tipos de datos

        //Enteros
        byte num1 = 1;
        System.out.println("Numero entero byte: " + num1);
        short num2 = 2;
        System.out.println("Numero entero short: " + num2);
        int num3 = 3;
        System.out.println("Numero entero int: " + num3);
        long num4 = 4L;
        System.out.println("Numero entero long: " + num4);

        //Decimales
        double num5 = 1.1;
        System.out.println("Numero Decimal double: " + num5);
        float num6 = 2.1f;
        System.out.println("Numero Decimal float: " + num6);

        //Boolean
        boolean bool = false;
        System.out.println("Boolean: " + bool);

        //Caracter
        char caracter = 'a';
        System.out.println("Caracter char: " + caracter);

        //Cadena de texto
        String text = "Hola";
        System.out.println("Cadena de texto String: " + text);
    }
}